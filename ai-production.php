            const sliders = document.querySelectorAll('.media-slider');

            sliders.forEach(slider => {
                let isDown = false;
                let startX;
                let scrollLeft;

                // Mouse Drag Scrolling
                slider.addEventListener('mousedown', (e) => {
                    isDown = true;
                    startX = e.pageX - slider.offsetLeft;
                    scrollLeft = slider.scrollLeft;
                    slider.style.scrollSnapType = 'none'; // Disable snap during drag
                });

                slider.addEventListener('mouseleave', () => isDown = false);
                slider.addEventListener('mouseup', () => {
                    isDown = false;
                    slider.style.scrollSnapType = 'x mandatory'; // Re-enable snap
                });

                slider.addEventListener('mousemove', (e) => {
                    if (!isDown) return;
                    e.preventDefault();
                    const x = e.pageX - slider.offsetLeft;
                    const walk = (x - startX) * 2;
                    slider.scrollLeft = scrollLeft - walk;
                });

                //  Video Play Logic (Intersection Observer)   
                const videoObserver = new IntersectionObserver((entries) => {
                    entries.forEach(entry => {
                        const video = entry.target;


                        if (entry.isIntersecting && entry.intersectionRatio >= 0.9) {

                            if (video.closest('.panel').matches(':hover')) {
                                video.play().catch(() => {});
                            }
                        } else {
                            video.pause();
                            video.currentTime = 0;
                        }
                    });
                }, {
                    threshold: 0.9
                });

                slider.querySelectorAll('video').forEach(vid => videoObserver.observe(vid));

                //  Panel Hover To Play   
                const panel = slider.closest('.panel');
                panel.addEventListener('mouseenter', () => {

                    const visibleVideo = Array.from(slider.querySelectorAll('video')).find(v => {
                        const rect = v.getBoundingClientRect();
                        return rect.left >= 0 && rect.right <= window.innerWidth;
                    });
                    if (visibleVideo) visibleVideo.play();
                });

                panel.addEventListener('mouseleave', () => {
                    slider.querySelectorAll('video').forEach(v => v.pause());
                });
            });
        });

