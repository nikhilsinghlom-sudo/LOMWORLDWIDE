    <script>
        const cards = document.querySelectorAll('.testimonial-card');
        const dots = document.querySelectorAll('.dot');
        const slider = document.querySelector('.testimonial-slider');
        const prevBtn = document.querySelector('.slider-arrow.left');
        const nextBtn = document.querySelector('.slider-arrow.right');

        // Default: First card for mobile, Second card (index 1) for Desktop
        let currentIndex = window.innerWidth <= 768 ? 0 : 1;

        function updateSlider(index) {
            cards.forEach((card, i) => {
                card.classList.toggle('active', i === index);
            });

            dots.forEach((dot, i) => {
                dot.classList.toggle('active', i === index);
            });

            const cardWidth = cards[0].offsetWidth;
            const gap = 30; // Matches CSS gap
            const stepSize = cardWidth + gap;
            const wrapperWidth = slider.parentElement.offsetWidth;

            //Calculates the left-padding needed to center the active card
            const centerOffset = (wrapperWidth / 2) - (cardWidth / 2);

            slider.style.transform = `translateX(${centerOffset - (stepSize * index)}px)`;
        }

        nextBtn.addEventListener('click', () => {
            currentIndex = (currentIndex + 1) % cards.length;
            updateSlider(currentIndex);
        });

        prevBtn.addEventListener('click', () => {
            currentIndex = (currentIndex - 1 + cards.length) % cards.length;
            updateSlider(currentIndex);
        });

        dots.forEach((dot, i) => {
            dot.addEventListener('click', () => {
                currentIndex = i;
                updateSlider(currentIndex);
            });
        });

        // Initial call
        updateSlider(currentIndex);

        // Handle screen resize
        window.addEventListener('resize', () => {
            // Update logic if screen crosses the mobile threshold during resize
            if (window.innerWidth <= 768 && currentIndex === 1 && !cards[1].classList.contains('active')) {
                currentIndex = 0;
            }
            updateSlider(currentIndex);
        });
